import csv
from pathlib import Path
from datetime import datetime, timezone

import matplotlib
import matplotlib.pyplot as plt
import matplotlib.ticker as ticker
import numpy as np

from utils.logger_utils import Logger
from utils.path_utils import PathUtils

matplotlib.use("Agg")


class Visualizer:
    """
    Reads the CSV files generated by Evaluator, MidTrainingCallback, and YOLO
    itself, then produces publication-quality graphs for supervisor presentations
    and project reports.

    Graphs produced:
        1. per_class_f1_bar.png                  — horizontal bar chart, F1 per class
        2. per_class_precision_recall_bar.png     — grouped P vs R per class
        3. per_class_map50_bar.png                — horizontal bar chart, mAP50 per class
        4. iteration_loss_curves.png              — box/cls/dfl loss across all iterations
        5. epoch_class_f1_progression.png         — F1 per class across epochs
        6. epoch_map50_progression.png            — overall mAP50 across epochs
        7. train_val_loss_comparison.png          — train vs val loss, overfitting detection

    Sources:
        - evaluation_results/per_class_metrics_TIMESTAMP.csv     (from evaluator.py)
        - evaluation_results/iteration_losses_TIMESTAMP.csv      (from mid_training_callback.py)
        - evaluation_results/epoch_per_class_metrics_TIMESTAMP.csv (from mid_training_callback.py)
        - checkpoints/train/results.csv                          (from YOLO automatically)

    All graphs saved to: evaluation_results/graphs/
    """

    # Color palette — consistent across all graphs
    COLORS: list = [
        "#2E86AB", "#A23B72", "#F18F01", "#C73E1D", "#3B1F2B",
        "#44BBA4", "#E94F37", "#393E41", "#F5A65B", "#B5BD89",
        "#6B4226", "#7B2D8B", "#1A535C", "#FF6B6B",
    ]

    def __init__(self):
        log_namespace = self.__class__.__name__
        self.logger = Logger(log_namespace, f"{log_namespace}.log").get()
        self.eval_dir: Path = PathUtils().get_base_path().joinpath("evaluation_results")
        self.graphs_dir: Path = self.eval_dir.joinpath("graphs")
        self.graphs_dir.mkdir(parents=True, exist_ok=True)

    def _find_latest_csv(self, prefix: str) -> Path:
        """
        Finds the most recently created CSV file matching the given prefix
        inside evaluation_results/. Since filenames contain timestamps in
        YYYYMMDD_HHMMSS format, alphabetical sort equals chronological sort.

        Args:
            prefix (str): Filename prefix to search for, e.g. "per_class_metrics".

        Returns:
            Path: Path to the most recent matching CSV file.

        Raises:
            FileNotFoundError: If no matching CSV file is found.
        """
        self.logger.info(f"inside _find_latest_csv method — searching for prefix: {prefix}..........")
        try:
            matches: list = sorted(self.eval_dir.glob(f"{prefix}_*.csv"))
            if not matches:
                raise FileNotFoundError(
                    f"No CSV file found with prefix '{prefix}' in {self.eval_dir}. "
                    f"Run evaluator.py or train with callbacks first."
                )
            latest: Path = matches[-1]
            self.logger.info(f"Found latest CSV: {latest.name}")
            return latest
        except FileNotFoundError:
            raise
        except Exception as e:
            self.logger.exception(f"Error finding CSV with prefix {prefix}: {e}")
            raise

    def _read_csv_as_dicts(self, file_path: Path) -> list:
        """
        Reads a CSV file and returns its contents as a list of dicts where
        each dict represents one row with column names as keys.

        Args:
            file_path (Path): Path to the CSV file to read.

        Returns:
            list: List of dicts, one per row. All values are strings —
                  callers must cast to float/int as needed.

        Raises:
            Exception: If file cannot be read or parsed.
        """
        self.logger.info(f"inside _read_csv_as_dicts method — reading {file_path.name}..........")
        rows: list = []
        try:
            with open(file_path, "r", encoding="utf-8") as f:
                reader = csv.DictReader(f)
                rows = [row for row in reader]
            self.logger.info(f"Read {len(rows)} rows from {file_path.name}")
        except Exception as e:
            self.logger.exception(f"Error reading CSV {file_path}: {e}")
            raise
        return rows

    def _save_figure(self, fig: plt.Figure, filename: str) -> None:
        """
        Saves a matplotlib figure to the graphs directory at high resolution
        suitable for reports and presentations. Closes the figure after saving
        to release memory.

        Args:
            fig (plt.Figure): Matplotlib figure object to save.
            filename (str): Output filename including extension, e.g. "f1_bar.png".

        Returns:
            None
        """
        self.logger.info(f"inside _save_figure method — saving {filename}..........")
        try:
            output_path: Path = self.graphs_dir.joinpath(filename)
            fig.savefig(str(output_path), dpi=150, bbox_inches="tight", facecolor="white")
            plt.close(fig)
            self.logger.info(f"Graph saved to {output_path}")
            print(f"  Saved: {output_path.name}")
        except Exception as e:
            self.logger.exception(f"Error saving figure {filename}: {e}")

    # -------------------------------------------------------------------------
    # Graph 1 — Per-class F1 horizontal bar chart
    # -------------------------------------------------------------------------

    def plot_per_class_f1(self) -> None:
        """
        Reads per_class_metrics CSV and plots a horizontal bar chart showing
        F1 score for each of the 14 classes sorted from highest to lowest.
        F1 is the most honest single-number metric per class so this is the
        primary graph for your supervisor meeting. Bars under 0.5 are colored
        red to immediately highlight weak classes.

        Args:
            None

        Returns:
            None
        """
        self.logger.info("inside plot_per_class_f1 method..........")
        try:
            csv_path: Path = self._find_latest_csv("per_class_metrics")
            rows: list = self._read_csv_as_dicts(csv_path)

            class_names: list = [r["class"] for r in rows]
            f1_scores: list = [float(r["f1"]) for r in rows]

            # Sort by F1 descending so best classes appear at top
            paired: list = sorted(zip(f1_scores, class_names), reverse=True)
            f1_sorted: list = [p[0] for p in paired]
            names_sorted: list = [p[1] for p in paired]

            bar_colors: list = [
                "#C73E1D" if score < 0.5 else "#2E86AB" for score in f1_sorted
            ]

            fig, ax = plt.subplots(figsize=(10, 7))
            bars = ax.barh(
                names_sorted, f1_sorted,
                color=bar_colors, height=0.6, edgecolor="white"
            )

            # Value labels at end of each bar
            for bar, score in zip(bars, f1_sorted):
                ax.text(
                    bar.get_width() + 0.01,
                    bar.get_y() + bar.get_height() / 2,
                    f"{score:.3f}",
                    va="center",
                    fontsize=9,
                    color="#333333",
                )

            ax.set_xlabel("F1 Score", fontsize=11)
            ax.set_title(
                "Per-Class F1 Score — Test Set\n(Red = F1 below 0.5, needs improvement)",
                fontsize=13,
                fontweight="bold",
                pad=15,
            )
            ax.set_xlim(0, 1.1)
            ax.xaxis.set_major_formatter(ticker.FormatStrFormatter("%.1f"))
            ax.axvline(
                x=0.5, color="#999999", linestyle="--",
                linewidth=1, label="0.5 threshold"
            )
            ax.legend(fontsize=9)
            ax.invert_yaxis()
            ax.grid(axis="x", alpha=0.3)
            fig.tight_layout()

            self._save_figure(fig, "per_class_f1_bar.png")

        except Exception as e:
            self.logger.exception(f"Error in plot_per_class_f1: {e}")

    # -------------------------------------------------------------------------
    # Graph 2 — Per-class Precision vs Recall grouped bar chart
    # -------------------------------------------------------------------------

    def plot_precision_recall_comparison(self) -> None:
        """
        Reads per_class_metrics CSV and plots a grouped bar chart showing
        precision and recall side by side for each class. This graph explains
        to your supervisor whether the model is trigger-happy (high recall,
        low precision) or conservative (high precision, low recall) per class.

        Args:
            None

        Returns:
            None
        """
        self.logger.info("inside plot_precision_recall_comparison method..........")
        try:
            csv_path: Path = self._find_latest_csv("per_class_metrics")
            rows: list = self._read_csv_as_dicts(csv_path)

            class_names: list = [r["class"] for r in rows]
            precisions: list = [float(r["precision"]) for r in rows]
            recalls: list = [float(r["recall"]) for r in rows]

            x: np.ndarray = np.arange(len(class_names))
            bar_width: float = 0.35

            fig, ax = plt.subplots(figsize=(14, 6))
            ax.bar(
                x - bar_width / 2, precisions, bar_width,
                label="Precision", color="#2E86AB", edgecolor="white"
            )
            ax.bar(
                x + bar_width / 2, recalls, bar_width,
                label="Recall", color="#F18F01", edgecolor="white"
            )

            ax.set_xticks(x)
            ax.set_xticklabels(class_names, rotation=35, ha="right", fontsize=9)
            ax.set_ylabel("Score", fontsize=11)
            ax.set_ylim(0, 1.15)
            ax.set_title(
                "Precision vs Recall Per Class — Test Set",
                fontsize=13,
                fontweight="bold",
                pad=15,
            )
            ax.legend(fontsize=10)
            ax.grid(axis="y", alpha=0.3)
            fig.tight_layout()

            self._save_figure(fig, "per_class_precision_recall_bar.png")

        except Exception as e:
            self.logger.exception(f"Error in plot_precision_recall_comparison: {e}")

